// Operadores lógicos (logical operator) - &&, || e !

// - 2 valores booleanos, quando verificados
// resultará em vardeira ou falso

let pao = true
let queijo = false

// AND &&
console.log(pao && queijo) //as duas variáveis tem que ser verdadeira para a  resposta ser verdadeira

// OR ||
console.log(pao || queijo) //qualquer um que for verdadeiradeiro, dá verdadeiro

// NOT !
console.log(!pao) //quando botamos exclamação na frente de uma variável estamos negando/trocando o valor dela. nesse caso é false.